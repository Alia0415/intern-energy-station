/**
 * 按问题 + 角色检索规则库（注意参数顺序：question 在前，role 在后）。
 * @param {string} question 用户问题
 * @param {string} role  当前身份：intern | mentor | hr
 * @param {number} limit  返回上限（默认 3，即“2-3 条”）
 * @returns {Array<{id,roles,title,content,score,hits}>} 命中的 matchedDocs（已按相关度排序）
 */
export function searchKnowledgeBase(question, role, limit = 3) {
  const q = String(question || "").toLowerCase();
  if (!q.trim() || !role) return [];
  return KNOWLEDGE_BASE.filter((r) => Array.isArray(r.roles) && r.roles.includes(role))
    .map((rule) => {
      let score = 0;
      const hits = [];
      for (const tag of rule.tags) {
        if (q.includes(String(tag).toLowerCase())) {
          score += String(tag).length; // 命中越具体（越长）权重越高
          hits.push(tag);
        }
      }
      return { rule, score, hits };
    })
    .filter((x) => x.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, limit)
    .map(({ rule, score, hits }) => ({
      id: rule.id,
      roles: rule.roles,
      title: rule.title,
      content: rule.content,
      score,
      hits,
    }));
}

/** 取规则正文的首句（用于兜底摘要） */
function firstSentence(text) {
  const parts = String(text || "").split(/[。；\n]/).filter(Boolean);
  return ((parts[0] || text || "").trim()) + "。";
}

/**
 * 失败兜底：把命中的规则原文拼成可读的本地回答（前后端共用，保证一致）。
 * @param {Array<{title,content}>} matchedDocs
 */
export function buildFallbackAnswer(matchedDocs) {
  return (matchedDocs || []).map((d) => `· ${d.title}：${firstSentence(d.content)}`).join("\n");
}
